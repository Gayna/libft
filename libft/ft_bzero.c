#include "libft.h"

void		ft_bzero(void *s1, size_t n)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = (char *) s1;
	while (i < n)
	{
		tmp[i] = 0;
		i++;
	}
	return;
}
